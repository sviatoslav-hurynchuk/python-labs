import random


def task1():
    print("\n" + "=" * 60)
    print("ЗАВДАННЯ 1: Максимальний елемент та зворотній порядок")
    print("=" * 60)

    n = int(input("Введіть кількість елементів списку: "))
    if n <= 0:
        print("Кількість елементів має бути більше нуля!")
        return

    lst = []
    print("Введіть елементи списку:")
    for i in range(n):
        elem = int(input(f"Елемент {i + 1}: "))
        lst.append(elem)

    max_elem = max(lst)
    reversed_lst = lst[::-1]

    print("-" * 60)
    print(f"Максимальний елемент: {max_elem}")
    print(f"Список у зворотному порядку: {reversed_lst}")
    print("=" * 60)

